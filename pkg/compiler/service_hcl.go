// templateFuncs provides helper functions used in service.hcl templates.
var templateFuncs = template.FuncMap{
	// sgRuleHCL serializes a SGRule into an HCL object literal.
	"sgRuleHCL": func(r SGRule) string {
		cidrs := ""
		for i, c := range r.CIDRBlocks {
			if i > 0 {
				cidrs += ", "
			}
			cidrs += fmt.Sprintf("%q", c)
		}
		return fmt.Sprintf(
			"{ from_port = %d, to_port = %d, protocol = %q, cidr_blocks = [%s], description = %q }",
			r.FromPort, r.ToPort, r.Protocol, cidrs, r.Description,
		)
	},
	// joinStrings produces a comma-separated HCL list of quoted strings.
	"joinStrings": func(ss []string) string {
		quoted := make([]string, len(ss))
		for i, s := range ss {
			quoted[i] = fmt.Sprintf("%q", s)
		}
		return strings.Join(quoted, ", ")
	},
	// volumeName converts an absolute path to a valid ECS volume name.
	// e.g. "/tmp" -> "vol-tmp", "/workspace/data" -> "vol-workspace-data"
	"volumeName": func(path string) string {
		// Strip leading slash, replace remaining slashes with dashes
		name := strings.TrimPrefix(path, "/")
		name = strings.ReplaceAll(name, "/", "-")
		return "vol-" + name
	},
}

// joinGitHubAllowedReposCSV returns the AllowedRepos slice as a comma-separated string
// suitable for the KM_GITHUB_ALLOWED_REPOS proxy environment variable.
// Returns empty string when GitHub config is nil or AllowedRepos is empty.
func joinGitHubAllowedReposCSV(p *profile.SandboxProfile) string {
	if p.Spec.SourceAccess.GitHub == nil || len(p.Spec.SourceAccess.GitHub.AllowedRepos) == 0 {
		return ""
	}
	return strings.Join(p.Spec.SourceAccess.GitHub.AllowedRepos, ",")
}

