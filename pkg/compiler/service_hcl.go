// generateEC2ServiceHCL produces the service.hcl content for an EC2/ec2spot sandbox.
// NetworkConfig holds shared VPC/subnet info from km init.
type NetworkConfig struct {
	VPCID             string
	PublicSubnets     []string
	AvailabilityZones []string
	RegionLabel       string
	// EmailDomain is the sandboxes subdomain (e.g. "sandboxes.klankermaker.ai").
	// When empty, defaults to "sandboxes.klankermaker.ai". Derived from Config.Domain
	// as "sandboxes."+domain so forks use their own domain without code changes.
	EmailDomain string
	// ArtifactsBucket is the S3 bucket for sidecars, bootstrap scripts, etc.
	ArtifactsBucket string
	// SpotRateUSD is the resolved spot instance hourly rate in USD.
	// Populated by create.go from the AWS Pricing API (or static fallback table)
	// before calling Compile(). Zero when no budget or rate lookup is skipped.
	SpotRateUSD float64
	// Alias is the optional human-friendly sandbox alias (e.g. "orc-1").
	// When set, the sandbox can also receive and send email as {alias}@{emailDomain}.
	Alias string
}

// budgetHCLFields extracts the budget-related template fields from a SandboxProfile.
// Returns zero values when budget is nil (enforcement disabled).
func budgetHCLFields(p *profile.SandboxProfile) (hasBudget bool, computeLimit, aiLimit, warningThreshold float64) {
	if p.Spec.Budget == nil {
		return false, 0, 0, 0
	}
	hasBudget = true
	threshold := p.Spec.Budget.WarningThreshold
	if threshold <= 0 {
		threshold = 0.8
	}
	if p.Spec.Budget.Compute != nil {
		computeLimit = p.Spec.Budget.Compute.MaxSpendUSD
	}
	if p.Spec.Budget.AI != nil {
		aiLimit = p.Spec.Budget.AI.MaxSpendUSD
	}
	return hasBudget, computeLimit, aiLimit, threshold
}

func generateEC2ServiceHCL(p *profile.SandboxProfile, sandboxID string, useSpot bool, sgRules []SGRule, iamPolicy *IAMSessionPolicy, userData string, network *NetworkConfig) (string, error) {
	tmpl, err := template.New("ec2-service-hcl").Funcs(templateFuncs).Parse(ec2ServiceHCLTemplate)
	if err != nil {
		return "", fmt.Errorf("parse ec2 service.hcl template: %w", err)
	}

	hasBudget, computeLimit, aiLimit, warningThreshold := budgetHCLFields(p)

	params := ec2HCLParams{
		EnableBedrock:     p.Spec.Execution.UseBedrock,
		ProfileName:       p.Metadata.Name,
		SandboxID:         sandboxID,
		Region:            p.Spec.Runtime.Region,
		RegionLabel:       network.RegionLabel,
		InstanceType:      p.Spec.Runtime.InstanceType,
		UseSpot:           useSpot,
		UserDataBase64:    userData,
		VPCID:             network.VPCID,
		PublicSubnets:     network.PublicSubnets,
		AvailabilityZones: network.AvailabilityZones,
		SGEgressRules:     sgRules,
		IAMPolicy:         iamPolicy,
		// Budget enforcement fields
		HasBudget:        hasBudget,
		SpotRateUSD:      network.SpotRateUSD,
		ComputeLimit:     computeLimit,
		AILimit:          aiLimit,
		WarningThreshold: warningThreshold,
		CreatedAt:        time.Now().UTC().Format(time.RFC3339),
	}

	// Populate GitHub token fields when sourceAccess.github is configured with at least one repo.
	// Empty allowedRepos is treated as deny-by-default (same as nil github config).
	if p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRepos) > 0 {
		params.HasGitHub = true
		params.GitHubSSMPath = fmt.Sprintf("/sandbox/%s/github-token", sandboxID)
		params.GitHubAllowedRepos = p.Spec.SourceAccess.GitHub.AllowedRepos
		compiledPerms := pkggithub.CompilePermissions(nil)
		params.GitHubPermissions = permissionsToHCL(compiledPerms)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, params); err != nil {
		return "", fmt.Errorf("execute ec2 service.hcl template: %w", err)
	}
	return buf.String(), nil
}

