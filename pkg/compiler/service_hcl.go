const ecsDefaultMemory = 2048

// generateECSServiceHCL produces the service.hcl content for an ECS/ecs-cluster sandbox.
func generateECSServiceHCL(p *profile.SandboxProfile, sandboxID string, useSpot bool, sgRules []SGRule, network *NetworkConfig) (string, error) {
	tmpl, err := template.New("ecs-service-hcl").Funcs(templateFuncs).Parse(ecsServiceHCLTemplate)
	if err != nil {
		return "", fmt.Errorf("parse ecs service.hcl template: %w", err)
	}

	// Main container gets the majority of CPU/memory; sidecars share the rest.
	// Total: 1024 CPU, 2048 MiB memory
	// Sidecars: 2x128 CPU + 2x64 CPU = 384 CPU, 2x256 + 2x128 = 768 MiB
	// Main gets the remainder: 640 CPU, 1280 MiB
	mainCPU := ecsDefaultCPU - (128 + 128 + 64 + 64)
	mainMemory := ecsDefaultMemory - (256 + 256 + 128 + 128)
	mainMemoryReservation := mainMemory / 2

	// Email domain for Phase 4: use domain from NetworkConfig when set, otherwise default.
	emailDomain := "sandboxes.klankermaker.ai"
	if network != nil && network.EmailDomain != "" {
		emailDomain = network.EmailDomain
	}
	artifactBucket := os.Getenv("KM_ARTIFACTS_BUCKET")
	// No hardcoded fallback — artifacts_bucket should be set via km-config.yaml or env var.

	hasBudget, computeLimit, aiLimit, warningThreshold := budgetHCLFields(p)

	// Compute ECR image URIs for sidecar containers (PROV-10).
	// KM_ACCOUNTS_APPLICATION provides the AWS account ID for the ECR registry.
	// KM_SIDECAR_VERSION controls the image tag; defaults to "latest" when unset.
	accountID := os.Getenv("KM_ACCOUNTS_APPLICATION")
	imageTag := os.Getenv("KM_SIDECAR_VERSION")
	if imageTag == "" {
		imageTag = "latest"
	}
	var ecrRegistry string
	if accountID != "" {
		ecrRegistry = fmt.Sprintf("%s.dkr.ecr.%s.amazonaws.com", accountID, p.Spec.Runtime.Region)
	} else {
		ecrRegistry = "PLACEHOLDER_ECR"
	}
	sidecarImage := func(name string) string {
		return ecrRegistry + "/km-" + name + ":" + imageTag
	}

	// mainImage uses the same ECR registry as sidecars — km-sandbox:{version}
	// This replaces the MAIN_IMAGE_PLACEHOLDER that existed before the sandbox
	// container image was created in Phase 36.
	mainImage := sidecarImage("sandbox")

	params := ecsHCLParams{
		ProfileName:           p.Metadata.Name,
		SandboxID:             sandboxID,
		Region:                p.Spec.Runtime.Region,
		RegionLabel:           network.RegionLabel,
		TaskCPU:               ecsDefaultCPU,
		TaskMemory:            ecsDefaultMemory,
		MainImage:             mainImage,
		MainCPU:               mainCPU,
		MainMemory:            mainMemory,
		MainMemoryReservation: mainMemoryReservation,
		UseSpot:               useSpot,
		VPCID:                 network.VPCID,
		PublicSubnets:         network.PublicSubnets,
		SGEgressRules:         sgRules,
		AllowedDNSSuffixes:    strings.Join(p.Spec.Network.Egress.AllowedDNSSuffixes, ","),
		AllowedHTTPHosts:      strings.Join(append(p.Spec.Network.Egress.AllowedHosts, p.Spec.Network.Egress.AllowedDNSSuffixes...), ","),
		GitHubAllowedReposCSV: joinGitHubAllowedReposCSV(p),
		ArtifactsBucket:       artifactBucket,
		// Sidecar ECR image URIs computed from KM_ACCOUNTS_APPLICATION + region + KM_SIDECAR_VERSION.
		DNSProxyImage:  sidecarImage("dns-proxy"),
		HTTPProxyImage: sidecarImage("http-proxy"),
		AuditLogImage:  sidecarImage("audit-log"),
		TracingImage:   sidecarImage("tracing"),
		// Email fields — every Phase 4 sandbox gets an email identity.
		HasEmail:       true,
		SandboxEmail:   sandboxID + "@" + emailDomain,
		ArtifactBucket: artifactBucket,
		// Budget enforcement fields
		HasBudget:        hasBudget,
		SpotRateUSD:      network.SpotRateUSD,
		ComputeLimit:     computeLimit,
		AILimit:          aiLimit,
		WarningThreshold: warningThreshold,
		CreatedAt:        time.Now().UTC().Format(time.RFC3339),
	}

	// Populate GitHub token fields when sourceAccess.github is configured with at least one repo.
	// Empty allowedRepos is treated as deny-by-default (same as nil github config).
	if p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRepos) > 0 {
		params.HasGitHub = true
		params.GitHubSSMPath = fmt.Sprintf("/sandbox/%s/github-token", sandboxID)
		params.GitHubAllowedRepos = p.Spec.SourceAccess.GitHub.AllowedRepos
		compiledPerms := pkggithub.CompilePermissions(nil)
		params.GitHubPermissions = permissionsToHCL(compiledPerms)
	}

	// Populate Phase 36 entrypoint env vars (km-sandbox container configuration).
	// KM_SANDBOX_ID and KM_ARTIFACTS_BUCKET are always set; others are conditional.
	params.KMSandboxID = sandboxID
	params.KMArtifactsBucket = artifactBucket
	if artifactBucket != "" {
		params.KMProxyCACertS3 = fmt.Sprintf("s3://%s/sidecars/km-proxy-ca.crt", artifactBucket)
	}
	// KM_SECRET_PATHS: comma-separated SSM paths from identity.allowedSecretPaths
	if len(p.Spec.Identity.AllowedSecretPaths) > 0 {
		params.KMSecretPaths = strings.Join(p.Spec.Identity.AllowedSecretPaths, ",")
	}
	// KM_OTP_PATHS: comma-separated OTP SSM paths from otp.secrets
	if p.Spec.OTP != nil && len(p.Spec.OTP.Secrets) > 0 {
		params.KMOTPPaths = strings.Join(p.Spec.OTP.Secrets, ",")
	}
	// KM_INIT_COMMANDS: base64-encoded JSON array of shell commands
	if len(p.Spec.Execution.InitCommands) > 0 {
		initJSON, err := json.Marshal(p.Spec.Execution.InitCommands)
		if err == nil {
			params.KMInitCommands = base64.StdEncoding.EncodeToString(initJSON)
		}
	}
	// KM_PROFILE_ENV: base64-encoded JSON map of env vars (includes Bedrock vars if useBedrock)
	profileEnv := mergeBedrockEnv(p)
	if len(profileEnv) > 0 {
		envJSON, err := json.Marshal(profileEnv)
		if err == nil {
			params.KMProfileEnv = base64.StdEncoding.EncodeToString(envJSON)
		}
	}
	// KM_OPERATOR_EMAIL: from environment variable
	params.KMOperatorEmail = os.Getenv("KM_OPERATOR_EMAIL")
	// KM_GITHUB_TOKEN_SSM and KM_GITHUB_ALLOWED_REFS: use the already-populated GitHub fields
	if params.HasGitHub {
		params.KMGitHubTokenSSM = params.GitHubSSMPath
		if p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRefs) > 0 {
			params.KMGitHubAllowedRefs = strings.Join(p.Spec.SourceAccess.GitHub.AllowedRefs, ",")
		}
	}

	// Populate Claude Code OTEL telemetry fields (OTEL-01, OTEL-06).
	// IsEnabled() returns true when ClaudeTelemetry is nil (default on).
	ct := p.Spec.Observability.ClaudeTelemetry
	params.ClaudeTelemetryEnabled = ct.IsEnabled()
	if ct != nil {
		params.ClaudeTelemetryLogPrompts = ct.LogPrompts
		params.ClaudeTelemetryLogToolDetails = ct.LogToolDetails
	}
	params.OTELResourceAttributes = fmt.Sprintf("sandbox_id=%s,profile_name=%s,substrate=ecs", sandboxID, p.Metadata.Name)

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, params); err != nil {
		return "", fmt.Errorf("execute ecs service.hcl template: %w", err)
	}
	return buf.String(), nil
}
