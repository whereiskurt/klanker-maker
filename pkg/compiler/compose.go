// generateDockerCompose renders the docker-compose.yml template with profile data.
func generateDockerCompose(p *profile.SandboxProfile, sandboxID string, network *NetworkConfig, secretPaths []string) (string, error) {
	// Encode initCommands as base64 JSON array
	initCmdsJSON, err := json.Marshal(p.Spec.Execution.InitCommands)
	if err != nil {
		return "", fmt.Errorf("marshal initCommands: %w", err)
	}
	initCmdsB64 := base64.StdEncoding.EncodeToString(initCmdsJSON)

	// Inject Bedrock env vars if useBedrock is set.
	profileEnv := mergeBedrockEnv(p)

	// Encode profile env as base64 JSON object
	profileEnvJSON, err := json.Marshal(profileEnv)
	if err != nil {
		return "", fmt.Errorf("marshal profile env: %w", err)
	}
	profileEnvB64 := base64.StdEncoding.EncodeToString(profileEnvJSON)

	// Budget fields
	budgetEnabled := p.Spec.Budget != nil
	budgetTable := ""
	if budgetEnabled {
		budgetTable = fmt.Sprintf("km-budget-%s", sandboxID)
	}

	// Network fields from NetworkConfig (fall back to empty strings)
	artifactsBucket := ""
	stateBucket := ""
	emailDomain := ""
	if network != nil {
		artifactsBucket = network.ArtifactsBucket
		emailDomain = network.EmailDomain
	}

	// CA cert and key S3 paths (constructed when artifactsBucket is known)
	proxyCACertS3 := ""
	proxyCAKeyS3 := ""
	if artifactsBucket != "" {
		proxyCACertS3 = fmt.Sprintf("s3://%s/sidecars/km-proxy-ca.crt", artifactsBucket)
		proxyCAKeyS3 = fmt.Sprintf("s3://%s/sidecars/km-proxy-ca.key", artifactsBucket)
	}

	// GitHub token SSM path
	gitHubTokenSSM := ""
	if p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRepos) > 0 {
		gitHubTokenSSM = fmt.Sprintf("/sandbox/%s/github-token", sandboxID)
	}

	// Allowed repos/refs
	allowedRepos := ""
	allowedRefs := ""
	if p.Spec.SourceAccess.GitHub != nil {
		allowedRepos = strings.Join(p.Spec.SourceAccess.GitHub.AllowedRepos, " ")
		allowedRefs = strings.Join(p.Spec.SourceAccess.GitHub.AllowedRefs, " ")
	}

	// Build image names — use ECR URIs if KM_ACCOUNTS_APPLICATION is set, otherwise local names.
	accountID := os.Getenv("KM_ACCOUNTS_APPLICATION")
	sidecarImg := func(name string) string {
		if accountID != "" {
			return fmt.Sprintf("%s.dkr.ecr.%s.amazonaws.com/km-%s:latest", accountID, p.Spec.Runtime.Region, name)
		}
		return "km-" + name + ":latest"
	}

	data := dockerComposeData{
		SandboxID:          sandboxID,
		MainImage:          sidecarImg("sandbox"),
		DNSProxyImage:      sidecarImg("dns-proxy"),
		HTTPProxyImage:     sidecarImg("http-proxy"),
		AuditLogImage:      sidecarImg("audit-log"),
		TracingImage:        sidecarImg("tracing"),
		Region:             p.Spec.Runtime.Region,
		AllowedDNSSuffixes: strings.Join(p.Spec.Network.Egress.AllowedDNSSuffixes, ","),
		AllowedHosts:       strings.Join(p.Spec.Network.Egress.AllowedHosts, ","),
		AllowedRepos:       allowedRepos,
		AllowedRefs:        allowedRefs,
		SecretPaths:        strings.Join(secretPaths, ","),
		InitCommands:       initCmdsB64,
		ProfileEnv:         profileEnvB64,
		BudgetEnabled:      budgetEnabled,
		BudgetTable:        budgetTable,
		HTTPSOnly:          p.Spec.Network.HTTPSOnly,
		ArtifactsBucket:    artifactsBucket,
		StateBucket:        stateBucket,
		EmailDomain:        emailDomain,
		ProxyCACertS3:      proxyCACertS3,
		ProxyCAKeyS3:       proxyCAKeyS3,
		GitHubTokenSSM:     gitHubTokenSSM,
		AWSProfile:         os.Getenv("KM_AWS_PROFILE"),
		HostAWSDir:         filepath.Join(os.Getenv("HOME"), ".aws"),
	}

	tmpl, err := template.New("docker-compose").Parse(dockerComposeTemplate)
	if err != nil {
		return "", fmt.Errorf("parse docker-compose template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("execute docker-compose template: %w", err)
	}

	return buf.String(), nil
}

// GenerateAuditInitScript returns a shell script that creates the named pipe
// and installs the shell audit hook + heartbeat. This script should be mounted
// into the main container and sourced from /etc/profile.d/ or the entrypoint.
//
// On Docker, the named pipe lives on a shared volume (audit-vol) mounted at
// /run/km/ in both the main container and km-audit-log container — mirroring
// the EC2 architecture where both processes share the host filesystem.
func GenerateAuditInitScript(sandboxID string) string {
	return fmt.Sprintf(`#!/bin/bash
# km-audit-init.sh — creates audit named pipe and installs shell hook.
# Generated by the klankrmkr profile compiler. DO NOT EDIT.
# Mount this into the main container and source from entrypoint or profile.d.

# Create named pipe if it doesn't already exist (audit-log sidecar reads from it).
if [ ! -p /run/km/audit-pipe ]; then
  mkdir -p /run/km
  mkfifo /run/km/audit-pipe
  chmod 666 /run/km/audit-pipe
fi

# Install shell audit hook into profile.d so every login shell captures commands.
cat > /etc/profile.d/km-audit.sh << 'HOOK'
# Guard: only install once per shell (profile.d can be sourced multiple times)
[ -n "$_KM_AUDIT_INSTALLED" ] && return 0
_KM_AUDIT_INSTALLED=1

_km_audit() {
  local cmd
  cmd=$(HISTTIMEFORMAT= history 1 | sed 's/^ *[0-9]* *//')
  [ -z "$cmd" ] && return 0
  cmd=$(printf '%%s' "$cmd" | sed 's/\\/\\\\/g; s/"/\\"/g; s/	/ /g' | head -c 500)
  printf '{"timestamp":"%%s","sandbox_id":"%%s","event_type":"command","source":"shell","detail":{"command":"%%s","user":"%%s"}}\n' \
    "$(date -u +%%Y-%%m-%%dT%%H:%%M:%%SZ)" "%s" "$cmd" "$(whoami)" \
    > /run/km/audit-pipe 2>/dev/null
}
PROMPT_COMMAND="_km_audit;${PROMPT_COMMAND}"

_km_heartbeat() {
  while true; do
    sleep 60
    printf '{"timestamp":"%%s","sandbox_id":"%%s","event_type":"heartbeat","source":"shell","detail":{}}\n' \
      "$(date -u +%%Y-%%m-%%dT%%H:%%M:%%SZ)" "%s" > /run/km/audit-pipe 2>/dev/null
  done
}
_km_heartbeat &
_KM_HEARTBEAT_PID=$!
trap 'kill $_KM_HEARTBEAT_PID 2>/dev/null' EXIT
HOOK
chmod 644 /etc/profile.d/km-audit.sh
`, sandboxID, sandboxID)
}
