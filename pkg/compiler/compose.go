// compileDocker handles the Docker substrate compilation path.
// It produces a docker-compose.yml with 6 services:
//   - main: the sandbox container (user workload)
//   - km-dns-proxy: DNS filtering sidecar with static IP 172.28.0.10
//   - km-http-proxy: HTTP/HTTPS egress proxy with allowlist enforcement
//   - km-audit-log: audit log shipping sidecar
//   - km-tracing: OTEL tracing sidecar
//   - km-cred-refresh: credential rotation loop (only container with operator keys)
//
// Credential isolation: only km-cred-refresh has AWS_ACCESS_KEY_ID/SECRET/SESSION.
// All other containers use AWS_SHARED_CREDENTIALS_FILE pointing to /creds/sandbox or /creds/sidecar.
func compileDocker(p *profile.SandboxProfile, sandboxID string, network *NetworkConfig) (*CompiledArtifacts, error) {
	secretPaths := compileSecrets(p)
	iamPolicy := compileIAMPolicy(p)

	composeYAML, err := generateDockerCompose(p, sandboxID, network, secretPaths)
	if err != nil {
		return nil, fmt.Errorf("generate docker-compose.yml: %w", err)
	}

	return &CompiledArtifacts{
		SandboxID:         sandboxID,
		DockerComposeYAML: composeYAML,
		SecretPaths:       secretPaths,
		IAMPolicy:         iamPolicy,
	}, nil
}

// dockerComposeData is the template data struct for generateDockerCompose.
type dockerComposeData struct {
	SandboxID          string
	MainImage          string
	DNSProxyImage      string
	HTTPProxyImage     string
	AuditLogImage      string
	TracingImage       string
	Region             string
	AllowedDNSSuffixes string // space-separated
	AllowedHosts       string // space-separated
	AllowedRepos       string // space-separated
	AllowedRefs        string // space-separated
	SecretPaths        string // comma-separated
	InitCommands       string // base64-encoded JSON array
	ProfileEnv         string // base64-encoded JSON object
	BudgetEnabled      bool
	BudgetTable        string
	HTTPSOnly          bool
	ArtifactsBucket    string
	StateBucket        string
	EmailDomain        string
	ProxyCACertS3      string
	ProxyCAKeyS3       string
	GitHubTokenSSM     string
	AWSProfile         string // operator's AWS profile name for cred-refresh
	HostAWSDir         string // path to host's ~/.aws directory
}

