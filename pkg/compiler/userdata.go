// parseIdleTimeoutMinutes converts a duration string like "2h" or "30m" to minutes.
// Returns 0 if the string is empty or unparseable.
func parseIdleTimeoutMinutes(s string) int {
	if s == "" {
		return 0
	}
	d, err := time.ParseDuration(s)
	if err != nil {
		return 0
	}
	return int(d.Minutes())
}

// userDataParams holds the template parameters for user-data generation.
type userDataParams struct {
	SandboxID          string
	SecretPaths        []string
	HasGitHub          bool
	HasAllowedRefs     bool   // true when allowedRefs is non-empty
	AllowedRefs        string // colon-separated list for KM_ALLOWED_REFS env var
	AllowedDNSSuffixes string // comma-separated, from profile.Network.Egress.AllowedDNSSuffixes
	AllowedHTTPHosts     string // comma-separated, from profile.Network.Egress.AllowedHosts
	GitHubAllowedRepos   string // comma-separated GitHub repos from profile.sourceAccess.github.allowedRepos
	KMArtifactsBucket    string // from config env var KM_ARTIFACTS_BUCKET
	// Filesystem enforcement (section 2.5)
	ReadOnlyPaths []string
	WritablePaths []string
	// Spot interruption handling (section 6.5)
	UseSpot           bool
	ArtifactPaths     []string
	ArtifactMaxSizeMB int
	// Email fields (MAIL-02 through MAIL-05)
	SandboxEmail         string // {sandbox-id}@{emailDomain} (config-derived)
	EmailDomain          string // e.g. "sandboxes.klankermaker.ai"
	NotificationsEmail   string // notifications@{emailDomain} — from address for spot notifications
	OperatorEmail        string // from KM_OPERATOR_EMAIL env var — for spot notification
	AWSRegion            string // from IMDS region — for spot notification ses send-email CLI call
	// Budget enforcement fields (BUDG-03, BUDG-07)
	BudgetEnabled bool   // true when profile.spec.budget is set
	BudgetTable   string // DynamoDB table name from KM_BUDGET_TABLE env var
	// Idle timeout (minutes) — passed to audit-log sidecar for SandboxIdle event detection
	IdleTimeoutMinutes int
	// OTPSecrets holds path + env name pairs for one-time-password secret injection.
	// Each entry is fetched from SSM and deleted after first read.
	OTPSecrets []otpSecret
	// Rsync is the name of a saved home snapshot to restore on boot.
	Rsync string
	// InitCommands is a list of shell commands to run after sidecar startup.
	InitCommands []string
	// InitScripts is a list of S3 keys for init scripts to download and execute.
	InitScripts []string
	// ProfileEnv holds key=value pairs from profile spec.execution.env to export
	// via /etc/profile.d/ so they're available in all login shells (SSM sessions).
	ProfileEnv map[string]string
	// Claude Code OTEL telemetry (OTEL-01, OTEL-06)
	ClaudeTelemetryEnabled        bool
	ClaudeTelemetryLogPrompts     bool
	ClaudeTelemetryLogToolDetails bool
	ProfileName                   string
	Substrate                     string
	// Enforcement selects the network enforcement mode: "proxy", "ebpf", or "both".
	// Defaults to "proxy" when the profile field is unset (backwards compatible).
	// eBPF enforcement is EC2-only in Phase 40.
	Enforcement string
}

// otpSecret holds an SSM path and derived env var name for an OTP secret.
type otpSecret struct {
	Path    string // SSM parameter path (e.g. /sandbox/sb-123/otp/github-token)
	EnvName string // derived env var name (e.g. KM_OTP_GITHUB_TOKEN)
}

// joinAllowedRefs returns the AllowedRefs slice as a colon-separated string
// suitable for the KM_ALLOWED_REFS environment variable.
// Returns empty string when GitHub config is nil or AllowedRefs is empty.
func joinAllowedRefs(p *profile.SandboxProfile) string {
	if p.Spec.SourceAccess.GitHub == nil || len(p.Spec.SourceAccess.GitHub.AllowedRefs) == 0 {
		return ""
	}
	return strings.Join(p.Spec.SourceAccess.GitHub.AllowedRefs, ":")
}

// joinGitHubAllowedRepos returns the AllowedRepos slice as a comma-separated string
// suitable for the KM_GITHUB_ALLOWED_REPOS environment variable.
// Returns empty string when GitHub config is nil or AllowedRepos is empty.
func joinGitHubAllowedRepos(p *profile.SandboxProfile) string {
	if p.Spec.SourceAccess.GitHub == nil || len(p.Spec.SourceAccess.GitHub.AllowedRepos) == 0 {
		return ""
	}
	return strings.Join(p.Spec.SourceAccess.GitHub.AllowedRepos, ",")
}

// generateUserData produces the EC2 bootstrap user-data.sh content for the given profile.
// It is only called for ec2 substrate sandboxes.
// artifactsBucket is from the KM_ARTIFACTS_BUCKET env var; may be empty string.
// useSpot indicates whether the EC2 instance uses spot pricing (enables spot poll loop).
// emailDomain is the sandboxes subdomain (e.g. "sandboxes.klankermaker.ai"); defaults to
// "sandboxes.klankermaker.ai" when not provided (variadic for backward compatibility).
func generateUserData(p *profile.SandboxProfile, sandboxID string, secretPaths []string, artifactsBucket string, useSpot bool, emailDomainOverride ...string) (string, error) {
	tmpl, err := template.New("userdata").Parse(userDataTemplate)
	if err != nil {
		return "", err
	}

	emailDomain := "sandboxes.klankermaker.ai"
	if len(emailDomainOverride) > 0 && emailDomainOverride[0] != "" {
		emailDomain = emailDomainOverride[0]
	}

	budgetTable := os.Getenv("KM_BUDGET_TABLE")
	if budgetTable == "" {
		budgetTable = "km-budgets"
	}

	params := userDataParams{
		SandboxID:          sandboxID,
		SecretPaths:        secretPaths,
		HasGitHub:          p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRepos) > 0,
		HasAllowedRefs:     p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRefs) > 0,
		AllowedRefs:        joinAllowedRefs(p),
		AllowedDNSSuffixes:  strings.Join(p.Spec.Network.Egress.AllowedDNSSuffixes, ","),
		AllowedHTTPHosts:    strings.Join(append(p.Spec.Network.Egress.AllowedHosts, p.Spec.Network.Egress.AllowedDNSSuffixes...), ","),
		GitHubAllowedRepos:  joinGitHubAllowedRepos(p),
		KMArtifactsBucket:   artifactsBucket,
		UseSpot:            useSpot,
		// Email fields — every sandbox gets an email identity.
		SandboxEmail:       sandboxID + "@" + emailDomain,
		EmailDomain:        emailDomain,
		NotificationsEmail: "notifications@" + emailDomain,
		OperatorEmail:      os.Getenv("KM_OPERATOR_EMAIL"),
		AWSRegion:          p.Spec.Runtime.Region,
		// Budget enforcement fields — enable when profile.spec.budget is set.
		BudgetEnabled: p.Spec.Budget != nil,
		BudgetTable:   budgetTable,
		// Idle timeout — converted from duration string to minutes for the sidecar.
		IdleTimeoutMinutes: parseIdleTimeoutMinutes(p.Spec.Lifecycle.IdleTimeout),
	}

	// Populate filesystem policy fields (nil-safe)
	if p.Spec.Policy.FilesystemPolicy != nil {
		params.ReadOnlyPaths = p.Spec.Policy.FilesystemPolicy.ReadOnlyPaths
		params.WritablePaths = p.Spec.Policy.FilesystemPolicy.WritablePaths
	}

	// Populate artifact fields (nil-safe)
	if p.Spec.Artifacts != nil {
		params.ArtifactPaths = p.Spec.Artifacts.Paths
		params.ArtifactMaxSizeMB = p.Spec.Artifacts.MaxSizeMB
	}

	// Populate OTP secrets (nil-safe)
	if p.Spec.OTP != nil {
		for _, path := range p.Spec.OTP.Secrets {
			params.OTPSecrets = append(params.OTPSecrets, otpSecret{
				Path:    path,
				EnvName: otpEnvName(path),
			})
		}
	}

	// Populate rsync, init commands, and profile env vars
	params.Rsync = p.Spec.Execution.Rsync
	params.InitCommands = p.Spec.Execution.InitCommands
	params.InitScripts = p.Spec.Execution.InitScripts
	params.ProfileEnv = mergeBedrockEnv(p)

	// Populate Claude Code OTEL telemetry fields (OTEL-01, OTEL-06).
	// IsEnabled() returns true when ClaudeTelemetry is nil (default on).
	ct := p.Spec.Observability.ClaudeTelemetry
	params.ClaudeTelemetryEnabled = ct.IsEnabled()
	if ct != nil {
		params.ClaudeTelemetryLogPrompts = ct.LogPrompts
		params.ClaudeTelemetryLogToolDetails = ct.LogToolDetails
	}
	params.ProfileName = p.Metadata.Name
	params.Substrate = p.Spec.Runtime.Substrate

	// Populate enforcement mode — default to "proxy" for backwards compatibility.
	enforcement := p.Spec.Network.Enforcement
	if enforcement == "" {
		enforcement = "proxy"
	}
	params.Enforcement = enforcement

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, params); err != nil {
		return "", err
	}
	return buf.String(), nil
}
