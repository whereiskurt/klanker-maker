// joinAllowedRefs returns the AllowedRefs slice as a colon-separated string
// suitable for the KM_ALLOWED_REFS environment variable.
// Returns empty string when GitHub config is nil, AllowedRefs is empty, or
// AllowedRefs contains "*" (wildcard means all refs are allowed, no hook needed).
func joinAllowedRefs(p *profile.SandboxProfile) string {
	if p.Spec.SourceAccess.GitHub == nil || len(p.Spec.SourceAccess.GitHub.AllowedRefs) == 0 {
		return ""
	}
	for _, r := range p.Spec.SourceAccess.GitHub.AllowedRefs {
		if r == "*" {
			return ""
		}
	}
	return strings.Join(p.Spec.SourceAccess.GitHub.AllowedRefs, ":")
}

// joinAllowedSenders returns the AllowedSenders slice as a colon-separated string
// suitable for the KM_ALLOWED_SENDERS environment variable.
// Returns empty string when Email config is nil or AllowedSenders is empty.
func joinAllowedSenders(p *profile.SandboxProfile) string {
	if p.Spec.Email == nil || len(p.Spec.Email.AllowedSenders) == 0 {
		return ""
	}
	return strings.Join(p.Spec.Email.AllowedSenders, ":")
}

// joinGitHubAllowedRepos returns the AllowedRepos slice as a comma-separated string
// suitable for the KM_GITHUB_ALLOWED_REPOS environment variable.
// Returns empty string when GitHub config is nil or AllowedRepos is empty.
func joinGitHubAllowedRepos(p *profile.SandboxProfile) string {
	if p.Spec.SourceAccess.GitHub == nil || len(p.Spec.SourceAccess.GitHub.AllowedRepos) == 0 {
		return ""
	}
	return strings.Join(p.Spec.SourceAccess.GitHub.AllowedRepos, ",")
}

// boolToZeroOne converts a bool to "1" (true) or "0" (false).
// Used for KM_NOTIFY_ON_PERMISSION and KM_NOTIFY_ON_IDLE env vars.
func boolToZeroOne(b bool) string {
	if b {
		return "1"
	}
	return "0"
}

// mergeNotifyHookIntoSettings parses any user-supplied ~/.claude/settings.json
// from configFiles, appends the km-notify-hook commands to hooks.Notification
// and hooks.Stop arrays (preserving any existing entries), and writes the merged
// result back to configFiles. The km hook is always appended AFTER user hooks.
//
// Returns an error iff the user-supplied JSON is malformed.
// Always runs for every sandbox (HOOK-02: hook entries present unconditionally;
// runtime gating happens via env vars, not by omitting the settings.json entries).
func mergeNotifyHookIntoSettings(configFiles map[string]string) (map[string]string, error) {
	const settingsPath = "/home/sandbox/.claude/settings.json"

	if configFiles == nil {
		configFiles = map[string]string{}
	}

	var settings map[string]interface{}
	if existing, ok := configFiles[settingsPath]; ok && strings.TrimSpace(existing) != "" {
		if err := json.Unmarshal([]byte(existing), &settings); err != nil {
			return nil, fmt.Errorf("invalid JSON in spec.execution.configFiles[%q]: %w", settingsPath, err)
		}
	}
	if settings == nil {
		settings = map[string]interface{}{}
	}

	hooks, _ := settings["hooks"].(map[string]interface{})
	if hooks == nil {
		hooks = map[string]interface{}{}
	}

	// appendKMHook appends a km-notify-hook entry to the named event array.
	// Existing user entries are preserved at the front; km hook is appended last.
	appendKMHook := func(eventName, cmd string) {
		kmEntry := map[string]interface{}{
			"hooks": []interface{}{
				map[string]interface{}{"type": "command", "command": cmd},
			},
		}
		existing, _ := hooks[eventName].([]interface{})
		hooks[eventName] = append(existing, kmEntry)
	}

	appendKMHook("Notification", "/opt/km/bin/km-notify-hook Notification")
	appendKMHook("Stop", "/opt/km/bin/km-notify-hook Stop")

	settings["hooks"] = hooks

	merged, err := json.MarshalIndent(settings, "", "  ")
	if err != nil {
		return nil, fmt.Errorf("marshal merged settings.json: %w", err)
	}
	configFiles[settingsPath] = string(merged)
	return configFiles, nil
}

// buildL7ProxyHosts returns a comma-separated list of domain suffixes that require
// L7 proxy interception (connect4 DNAT rewrite) based on the profile configuration.
// Only domains that the profile actually accesses are included:
//   - GitHub domains when profile has sourceAccess.github configured
//   - Bedrock/Anthropic domains when profile has useBedrock: true
//
// The returned string is passed to km ebpf-attach --proxy-hosts in "ebpf" and
// "both" enforcement modes so the resolver marks resolved IPs for proxy redirect.
func buildL7ProxyHosts(p *profile.SandboxProfile) string {
	var hosts []string
	if p.Spec.SourceAccess.GitHub != nil {
		hosts = append(hosts, "github.com", "api.github.com", "raw.githubusercontent.com", "codeload.githubusercontent.com")
	}
	if p.Spec.Execution.UseBedrock {
		// .amazonaws.com covers Bedrock endpoints (bedrock-runtime.us-east-1.amazonaws.com etc.)
		// This is broader than strictly necessary, but non-Bedrock traffic passes through
		// the proxy transparently without MITM inspection.
		hosts = append(hosts, ".amazonaws.com", "api.anthropic.com")
	}
	return strings.Join(hosts, ",")
}

// generateUserData produces the EC2 bootstrap user-data.sh content for the given profile.
// It is only called for ec2 substrate sandboxes.
// artifactsBucket is from the KM_ARTIFACTS_BUCKET env var; may be empty string.
// useSpot indicates whether the EC2 instance uses spot pricing (enables spot poll loop).
// network carries EFSFilesystemID from efs/outputs.json when EFS is initialized; may be nil.
// emailDomainOverride is variadic for backward compatibility: [0]=emailDomain, [1]=alias.
func generateUserData(p *profile.SandboxProfile, sandboxID string, secretPaths []string, artifactsBucket string, useSpot bool, network *NetworkConfig, emailDomainOverride ...string) (string, error) {
	tmpl, err := template.New("userdata").Parse(userDataTemplate)
	if err != nil {
		return "", err
	}

	emailDomain := "sandboxes.klankermaker.ai"
	alias := ""
	if len(emailDomainOverride) > 0 && emailDomainOverride[0] != "" {
		emailDomain = emailDomainOverride[0]
	}
	if len(emailDomainOverride) > 1 && emailDomainOverride[1] != "" {
		alias = emailDomainOverride[1]
	}

	budgetTable := os.Getenv("KM_BUDGET_TABLE")
	if budgetTable == "" {
		budgetTable = "km-budgets"
	}

	params := userDataParams{
		SandboxID:          sandboxID,
		SecretPaths:        secretPaths,
		HasGitHub:          p.Spec.SourceAccess.GitHub != nil && len(p.Spec.SourceAccess.GitHub.AllowedRepos) > 0,
		HasAllowedRefs:     joinAllowedRefs(p) != "",
		AllowedRefs:        joinAllowedRefs(p),
		AllowedDNSSuffixes:  strings.Join(p.Spec.Network.Egress.AllowedDNSSuffixes, ","),
		AllowedHTTPHosts:    strings.Join(append(p.Spec.Network.Egress.AllowedHosts, p.Spec.Network.Egress.AllowedDNSSuffixes...), ","),
		GitHubAllowedRepos:  joinGitHubAllowedRepos(p),
		KMArtifactsBucket:   artifactsBucket,
		UseSpot:            useSpot,
		// Email fields — every sandbox gets an email identity.
		SandboxEmail:       sandboxID + "@" + emailDomain,
		EmailDomain:        emailDomain,
		Alias:              alias,
		AllowedSenders:     joinAllowedSenders(p),
		NotificationsEmail: "notifications@" + emailDomain,
		OperatorEmail:      os.Getenv("KM_OPERATOR_EMAIL"),
		AWSRegion:          p.Spec.Runtime.Region,
		// Budget enforcement fields — enable when profile.spec.budget is set.
		BudgetEnabled: p.Spec.Budget != nil,
		BudgetTable:   budgetTable,
		// Idle timeout — converted from duration string to minutes for the sidecar.
		IdleTimeoutMinutes: parseIdleTimeoutMinutes(p.Spec.Lifecycle.IdleTimeout),
		// IdleAction is "hibernate" when TTL=0 (empty string sentinel) and idle timeout is set.
		// This tells the audit-log sidecar to hibernate on idle instead of destroying.
		IdleAction: idleActionFromProfile(p),
	}

	// Populate alias email when alias is set.
	if alias != "" {
		params.AliasEmail = alias + "@" + emailDomain
	}

	// Populate artifact fields (nil-safe)
	if p.Spec.Artifacts != nil {
		params.ArtifactPaths = p.Spec.Artifacts.Paths
		params.ArtifactMaxSizeMB = p.Spec.Artifacts.MaxSizeMB
	}

	// Populate OTP secrets (nil-safe)
	if p.Spec.OTP != nil {
		for _, path := range p.Spec.OTP.Secrets {
			params.OTPSecrets = append(params.OTPSecrets, otpSecret{
				Path:    path,
				EnvName: otpEnvName(path),
			})
		}
	}

	// Populate rsync, init commands, and profile env vars
	params.Rsync = p.Spec.Execution.Rsync
	params.InitCommands = p.Spec.Execution.InitCommands
	params.InitScripts = p.Spec.Execution.InitScripts
	params.ConfigFiles = p.Spec.Execution.ConfigFiles
	params.ProfileEnv = mergeBedrockEnv(p)

	// Phase 62 (HOOK-03): populate NotifyEnv from spec.cli.notify* fields.
	// We emit env vars whenever Spec.CLI is non-nil (regardless of true/false booleans)
	// because Go's bool zero value + omitempty YAML tag cannot distinguish "unset"
	// from "explicit false". Behaviour: CLI block present → always emit ON_PERMISSION
	// and ON_IDLE (0 or 1); cooldown and email are conditional on the field being set.
	// This is a tracked deviation from CONTEXT.md's "iff field is set" literal wording.
	// See 62-02-SUMMARY.md for rationale.
	if p.Spec.CLI != nil {
		notifyEnv := map[string]string{
			"KM_NOTIFY_ON_PERMISSION": boolToZeroOne(p.Spec.CLI.NotifyOnPermission),
			"KM_NOTIFY_ON_IDLE":       boolToZeroOne(p.Spec.CLI.NotifyOnIdle),
		}
		if p.Spec.CLI.NotifyCooldownSeconds > 0 {
			notifyEnv["KM_NOTIFY_COOLDOWN_SECONDS"] = strconv.Itoa(p.Spec.CLI.NotifyCooldownSeconds)
		}
		if p.Spec.CLI.NotificationEmailAddress != "" {
			notifyEnv["KM_NOTIFY_EMAIL"] = p.Spec.CLI.NotificationEmailAddress
		}
		// Phase 63 — Slack-related env keys. *bool pointer semantics: nil = unset
		// (don't emit the env var; the hook's :-default takes effect for Phase 62 compat).
		// Non-nil = emit KEY=0|1.
		if p.Spec.CLI.NotifyEmailEnabled != nil {
			notifyEnv["KM_NOTIFY_EMAIL_ENABLED"] = boolToZeroOne(*p.Spec.CLI.NotifyEmailEnabled)
		}
		if p.Spec.CLI.NotifySlackEnabled != nil {
			notifyEnv["KM_NOTIFY_SLACK_ENABLED"] = boolToZeroOne(*p.Spec.CLI.NotifySlackEnabled)
		}
		// Compile-time channel pinning via NotifySlackChannelOverride. If unset,
		// Plan 08 (km create) injects KM_SLACK_CHANNEL_ID and KM_SLACK_BRIDGE_URL
		// at runtime by appending to /etc/profile.d/km-notify-env.sh post-launch.
		if p.Spec.CLI.NotifySlackChannelOverride != "" {
			notifyEnv["KM_SLACK_CHANNEL_ID"] = p.Spec.CLI.NotifySlackChannelOverride
		}
		// KM_SLACK_BRIDGE_URL is intentionally NOT emitted at compile time —
		// it requires a runtime SSM lookup of /km/slack/bridge-url. See Plan 08.

		// Phase 67: Slack inbound — emit KM_SLACK_THREADS_TABLE so the poller's
		// ${KM_SLACK_THREADS_TABLE:-km-slack-threads} fallback can see Phase 66
		// multi-instance overrides (e.g. stg-slack-threads). Also emit
		// KM_SLACK_INBOUND_QUEUE_URL as an empty placeholder; km create fills
		// it at runtime via SSM SendCommand after queue creation (Plan 67-06).
		if p.Spec.CLI.NotifySlackInboundEnabled {
			threadsTable := os.Getenv("KM_SLACK_THREADS_TABLE")
			if threadsTable == "" {
				threadsTable = "km-slack-threads"
			}
			notifyEnv["KM_SLACK_THREADS_TABLE"] = threadsTable
			// KM_SLACK_INBOUND_QUEUE_URL is a runtime value injected by km create.
			// Reserve the slot here so the env file always has the key; km create
			// overwrites the empty value after creating the SQS queue.
			notifyEnv["KM_SLACK_INBOUND_QUEUE_URL"] = ""
		}
		params.NotifyEnv = notifyEnv

		// Phase 67: wire SlackInboundEnabled for template conditionals and
		// SlackThreadsTableName for the systemd EnvironmentFile (informational).
		if p.Spec.CLI.NotifySlackInboundEnabled {
			params.SlackInboundEnabled = true
			threadsTable := os.Getenv("KM_SLACK_THREADS_TABLE")
			if threadsTable == "" {
				threadsTable = "km-slack-threads"
			}
			params.SlackThreadsTableName = threadsTable
		}

		// Phase 67 gap closure: replaces Phase 63 Step 11d ssm:SendCommand path.
		// Cloud-init polls SSM Parameter Store for the channel id (per-sandbox) and
		// the bridge URL (operator-wide) and writes them to a profile.d file the
		// poller / hooks can source. ssm:SendCommand is denied by an org-level SCP
		// for the application account, so the original push-into-the-instance
		// approach silently fails.
		if p.Spec.CLI.NotifySlackEnabled != nil && *p.Spec.CLI.NotifySlackEnabled {
			params.NotifySlackEnabled = true
		}
	}

	// Phase 62 (HOOK-02): merge km-notify-hook entries into ~/.claude/settings.json.
	// Always runs — every sandbox gets the hook wired into Claude Code settings.
	// Preserves any user-supplied hooks; km hook is appended after existing entries.
	// Fails fast if user-supplied JSON is malformed.
	mergedCF, err := mergeNotifyHookIntoSettings(params.ConfigFiles)
	if err != nil {
		return "", err
	}
	params.ConfigFiles = mergedCF

	// Populate Claude Code OTEL telemetry fields (OTEL-01, OTEL-06).
	// IsEnabled() returns true when ClaudeTelemetry is nil (default on).
	ct := p.Spec.Observability.ClaudeTelemetry
	params.ClaudeTelemetryEnabled = ct.IsEnabled()
	if ct != nil {
		params.ClaudeTelemetryLogPrompts = ct.LogPrompts
		params.ClaudeTelemetryLogToolDetails = ct.LogToolDetails
	}
	params.ProfileName = p.Metadata.Name
	params.Substrate = p.Spec.Runtime.Substrate

	// Populate enforcement mode — default to "proxy" for backwards compatibility.
	enforcement := p.Spec.Network.Enforcement
	if enforcement == "" {
		enforcement = "proxy"
	}
	params.Enforcement = enforcement

	// TLS uprobe observability — enable when profile has tlsCapture enabled.
	if p.Spec.Observability.TlsCapture.IsEnabled() {
		params.TLSEnabled = true
		params.TLSAllowedRepos = joinGitHubAllowedRepos(p)
	}

	// L7 proxy hosts — domain suffixes that require connect4 DNAT redirect to the L7 proxy.
	// Derived from profile fields (GitHub + Bedrock) rather than repo names.
	params.L7ProxyHosts = buildL7ProxyHosts(p)

	// Additional EBS volume mount point (Phase 33)
	if p.Spec.Runtime.AdditionalVolume != nil {
		params.AdditionalVolumeMountPoint = p.Spec.Runtime.AdditionalVolume.MountPoint
	}

	// EFS shared filesystem mount (Phase 43)
	// Only set when profile requests it AND the network config has a filesystem ID.
	if p.Spec.Runtime.MountEFS && network != nil && network.EFSFilesystemID != "" {
		params.EFSFilesystemID = network.EFSFilesystemID
		params.EFSMountPoint = p.Spec.Runtime.EFSMountPoint
		if params.EFSMountPoint == "" {
			params.EFSMountPoint = "/shared"
		}
	}

	// Privileged execution mode (Phase 47)
	params.Privileged = p.Spec.Execution.Privileged

	// Learn mode: enable --observe on eBPF enforcer for traffic recording.
	params.LearnMode = p.Spec.Observability.LearnMode

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, params); err != nil {
		return "", err
	}
	return buf.String(), nil
}
